package cart;

public class Cart_VO {
	int cart_seq;
	String cart_img;
	String cart_name;
	String cart_price;
	String cart_amount;
	String cart_size;
	String cart_color;
	String product_code;
	

	
	public String getCart_color() {
		return cart_color;
	}
	public void setCart_color(String cart_color) {
		this.cart_color = cart_color;
	}
	public int getCart_seq() {
		return cart_seq;
	}
	public void setCart_seq(int cart_seq) {
		this.cart_seq = cart_seq;
	}
	public String getProduct_code() {
		return product_code;
	}
	public void setProduct_code(String product_code) {
		this.product_code = product_code;
	}
	public String getCart_size() {
		return cart_size;
	}
	public void setCart_size(String cart_size) {
		this.cart_size = cart_size;
	}
	public String getCart_img() {
		return cart_img;
	}
	public void setCart_img(String cart_img) {
		this.cart_img = cart_img;
	}
	public String getCart_name() {
		return cart_name;
	}
	public void setCart_name(String cart_name) {
		this.cart_name = cart_name;
	}
	public String getCart_price() {
		return cart_price;
	}
	public void setCart_price(String cart_price) {
		this.cart_price = cart_price;
	}
	public String getCart_amount() {
		return cart_amount;
	}
	public void setCart_amount(String cart_amount) {
		this.cart_amount = cart_amount;
	}
	
	

}
